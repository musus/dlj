<h3>不具合かな？と思ったら…</h3>

<div class="bfb_each">

	<div class="bfb_q">フロートボタンが表示されません。</div>

	<div class="bfb_a">キャッシュが残っている可能性があります。<b>「WP Fastest Cache」「WP Super Cache」などのキャッシュプラグインを使用している場合は、プラグインの管理画面でキャッシュを消してください。</b></div>

</div>

<div class="bfb_each">

	<div class="bfb_q">下スクロールしてもフロートボタンが非表示になりません。</div>

	<div class="bfb_a">使用中のテーマや他のプラグインとの相性が悪い可能性があります。プラグインをひとつずつ停止させることで原因を特定できることがあります。<b>特に「WP Fastest Cache」「Autoptimize」などのプラグインでHTMLやJavaScriptを圧縮している場合に不具合が出やすいです。</b>その場合、HTMLやJavaScriptの圧縮機能をオフにしてお試しください。</div>

</div>

<div class="bfb_each">

	<div class="bfb_q">パソコンでは表示されますが、スマホだけ表示されません。</div>

	<div class="bfb_a">「開発モード」が「管理者のみ表示」になっている可能性が高いです。「管理者のみ表示」は管理者としてログインしていないと表示されないため、WPに管理者としてログインしていない状態のスマホブラウザでは表示されません。ご注意ください。</div>

</div>

<div class="bfb_each">

	<div class="bfb_q">フロートボタンが表示されなくなりました。</div>

	<div class="bfb_a">バツを押してフロートボタンを非表示にするとフロートボタンは7日間は表示されなくなります。再度表示するにはブラウザのCookie(bfb_closed)を削除してください。</div>

</div>

<div class="bfb_each">

	<div class="bfb_q">PRO版のカラーピッカーが表示されません。</div>

	<div class="bfb_a">WP Rocketなどのプラグインが干渉している可能性があります。JavaScript
	を圧縮させるプラグインとは相性が悪いため、設定時のみ該当プラグインを一時的に停止させてください。</div>

</div>

<div class="bfb_each">

	<div class="bfb_q">プラグインの更新に失敗しました。</div>

	<div class="bfb_a"><a href="/wp-admin/plugins.php" target="_blank">プラグイン画面</a>での更新は失敗することがあります。更新に失敗した場合、画面左上のダッシュボード内にある<a href="/wp-admin/update-core.php" target="_blank">更新ページ</a>よりプラグインを更新してください。</div>

</div>

<h3>使い方について</h3>

<div class="bfb_each">

	<div class="bfb_q">開発モードの使い方を教えてください。</div>

	<div class="bfb_a">開発モードを「管理者のみ表示」にするとフロートボタンはWordPressにログイン中の管理者のみにしか表示されません。<b>設定中の時など一般ユーザーに表示しなくない時は「管理者のみ表示」を選択してください。</b>一般ユーザーにもフロートボタンを表示するには「全体公開」を選択してください。</div>

</div>

<div class="bfb_each">

	<div class="bfb_q">特定記事のみ表示する方法を教えてください。</div>

	<div class="bfb_a">共通設定でPCとスマホのボタンデザインを「表示しない」を選択。該当記事の編集画面で「個別設定を優先」にすれば該当記事のみ選択することができます。なおこちらはPRO版のみ対応しています。無料版の場合は表示したくない記事のIDを除外記事ID設定していただくことになります。</div>

</div>

<div class="bfb_each">

	<div class="bfb_q">除外記事IDは固定ページにも対応していますか？</div>

	<div class="bfb_a">投稿、固定ページのどちらにも対応しています。</div>

</div>

<div class="bfb_each">

	<div class="bfb_q">プラグインを削除したら、設定データは消えますか？</div>

	<div class="bfb_a">プラグインを削除しても、設定データは保持されます。</div>

</div>

<div class="bfb_each">

	<div class="bfb_q">\(バックスラッシュ)や"(ダブルクオーテーション)がおかしくなります。</div>

	<div class="bfb_a">セキュリティ対策のため半角のバックスラッシュとダブルクオーテーションは使用できません。全角文字を使用するか他の文字を使用してください。</div>

</div>

<h3>PRO版について</h3>

<div class="bfb_each">

	<div class="bfb_q">「ライセンスキー」とはなんですか？</div>

	<div class="bfb_a">ライセンスキーを入力することで、機能制限を解除することができます。機能制限が解除されると、個別記事毎のフロートボタン設定、プレビュー機能を利用することができます。<b class="bfb_bg-yellow">機能制限を希望する場合は、有料のPRO版ライセンスキーをご購入ください。</b><a href="https://bfb-plugin.com/pro" target="_blank">» PRO版はコチラ</a></div>

</div>

<div class="bfb_each">

	<div class="bfb_q">ライセンスキーは他のブログでも使いまわせますか？</div>

	<div class="bfb_a">はい、購入したライセンスキーは他のブログでも使いまわして使ってもらうことができます。ブログ数に制限はありません。<b class="bfb_fnt-red">ただし、他人にライセンスキーを譲渡、共有、付与等はできません。</b></div>

</div>

